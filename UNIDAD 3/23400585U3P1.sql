SELECT
O.name AS 'TABLA',
S.name AS 'ESQUEMA',
U.name AS 'PROPIETARIO'
FROM SYS.objects O
INNER JOIN SYS.schemas S ON (O.schema_id=S.schema_id)
INNER JOIN SYS.sysusers U ON (U.uid=s.principal_id)
WHERE O.type='U'
ORDER BY S.name
